int main(void){
	unsigned char i, offset = 3, count = 0;
	MatrixLED_Init();
	while(1){
		for(i = 0; i < 8; i++){
			MatrixLED_ShowColimn(i, Animation[i + offset]);
		}
		count++;
		if(count > 10){
			count = 0;
			offset++;
			if(offset > 40)
				offset = 0;
		}
	}
}
